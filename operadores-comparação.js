/* OPERADORES DE COMPARAÇÃO
==   --> Igual // Ele compara o valor, mas não compara o tipo do valor
===  --> Totalmente igual // Ele verifica o tipo do dado!

!=   --> Diferente // Ele compara o valor, mas não compara o tipo do valor
!==  --> Totalmente diferente // Ele verifica o tipo do dado!
*/

const firtPerson = 'João'
const seccondPerson = 'Maria'


const firstNumber = "23"
const seccondNumber = 23

console.log(firstNumber === seccondNumber) // imprime false
// Totalmente igual // Ele verifica o tipo do dado!


console.log(firstNumber == seccondNumber) // imprime true
//Igual // Ele compara o valor, mas não compara o tipo do valor


console.log(firtPerson != seccondPerson) // imprime true
// Diferente // Ele compara o valor, mas não compara o tipo do valor

console.log(firstNumber !== seccondNumber) // imprime true
// totalmente diferente

